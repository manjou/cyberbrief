"""Render the briefing as Markdown and as a standalone HTML page."""

from __future__ import annotations

import html
from datetime import datetime, timezone

from .iso import GLOSSARY, map_controls


def _controls_line(item: dict) -> str:
    text = f"{item.get('title', '')} {item.get('summary', '')} {item.get('description', '')} {item.get('name', '')}"
    return ", ".join(f"{cid} {cname}" for cid, cname in map_controls(text))


def glossary_terms_used(stories: list[dict], cves: list[dict]) -> list[tuple[str, str]]:
    blob = " ".join(
        f"{i.get('title', '')} {i.get('summary', '')} {i.get('description', '')} {i.get('explanation', '')}"
        for i in stories + cves
    ).lower()
    used = [(term, desc) for term, desc in GLOSSARY.items() if term.lower() in blob]
    if cves:  # CVE section always benefits from the scoring primer
        for term in ("KEV", "EPSS", "CVSS"):
            if term not in [t for t, _ in used]:
                used.append((term, GLOSSARY[term]))
    return used


def to_markdown(stories: list[dict], cves: list[dict], errors: list[str]) -> str:
    today = datetime.now(timezone.utc).strftime("%A, %d %B %Y")
    lines = [f"# 🛡️ CyberBrief — {today}", "",
             "*Your daily security briefing, ranked by real-world urgency (KEV → EPSS → CVSS), explained for humans.*", ""]

    lines.append("## 🔥 Top stories\n")
    for i, s in enumerate(stories, 1):
        lines.append(f"### {i}. {s['title']}")
        lines.append(f"*{s['source']}* — [read more]({s['link']})\n")
        if s.get("explanation"):
            lines.append(f"{s['explanation']}\n")
        elif s.get("summary"):
            lines.append(f"{s['summary']}\n")
        controls = _controls_line(s)
        if controls:
            lines.append(f"> 📋 **ISO 27001:** {controls}\n")

    if cves:
        lines.append("## 🚨 CVEs that matter today\n")
        lines.append("| CVE | Why it ranks | CVSS | EPSS | Exploited? |")
        lines.append("|-----|--------------|------|------|------------|")
        for v in cves:
            why = v.get("name") or (v.get("description", "")[:80] + "…")
            kev = "⚠️ YES (KEV)" if v.get("kev") else "not yet"
            if v.get("ransomware"):
                kev += " · ransomware"
            cvss = f"{v['cvss']:.1f}" if v.get("cvss") else "–"
            lines.append(f"| **{v['cve']}** | {why} | {cvss} | {v['epss'] * 100:.0f}% | {kev} |")
        lines.append("")
        for v in cves:
            if v.get("explanation"):
                lines.append(f"**{v['cve']}** — {v['explanation']}\n")

    terms = glossary_terms_used(stories, cves)
    if terms:
        lines.append("## 📖 Jargon decoder\n")
        for term, desc in terms:
            lines.append(f"- **{term}** — {desc}")
        lines.append("")

    if errors:
        lines.append(f"<sub>Sources unavailable this run: {', '.join(errors)}</sub>\n")
    lines.append("---")
    lines.append("*Generated by [CyberBrief](https://github.com/manjou/cyberbrief) — free, open source, no AI required.*")
    return "\n".join(lines)


