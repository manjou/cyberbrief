def to_html(stories: list[dict], cves: list[dict], errors: list[str]) -> str:
    today = datetime.now(timezone.utc).strftime("%A, %d %B %Y")
    e = html.escape
    parts = [f"<!doctype html><html lang='en'><head><meta charset='utf-8'>",
             "<meta name='viewport' content='width=device-width, initial-scale=1'>",
             f"<title>CyberBrief — {e(today)}</title><style>{HTML_STYLE}</style></head><body><main>",
             f"<h1>🛡️ CyberBrief</h1><p class='sub'>{e(today)} · ranked by real-world urgency "
             "(KEV → EPSS → CVSS) · explained for humans</p>"]

    parts.append("<h2>🔥 Top stories</h2>")
    for s in stories:
        parts.append("<article>")
        parts.append(f"<h3><a href='{e(s['link'])}'>{e(s['title'])}</a></h3>")
        parts.append(f"<div class='src'>{e(s['source'])}</div>")
        body = s.get("explanation") or s.get("summary") or ""
        if body:
            parts.append(f"<p>{e(body)}</p>")
        controls = _controls_line(s)
        if controls:
            parts.append(f"<span class='iso'>📋 ISO 27001: {e(controls)}</span>")
        parts.append("</article>")

    if cves:
        parts.append("<h2>🚨 CVEs that matter today</h2><div class='tablewrap'><table>")
        parts.append("<tr><th>CVE</th><th>What</th><th>CVSS</th><th>EPSS</th><th>Exploited?</th></tr>")
        for v in cves:
            why = v.get("name") or (v.get("description", "")[:90] + "…")
            kev = "<span class='kev'>YES — in CISA KEV</span>" if v.get("kev") else "not yet"
            if v.get("ransomware"):
                kev += " · ransomware use"
            cvss = f"{v['cvss']:.1f}" if v.get("cvss") else "–"
            parts.append(f"<tr><td><strong>{e(v['cve'])}</strong></td><td>{e(why)}</td>"
                         f"<td>{cvss}</td><td>{v['epss'] * 100:.0f}%</td><td>{kev}</td></tr>")
        parts.append("</table></div>")
        for v in cves:
            if v.get("explanation"):
                parts.append(f"<p><strong>{e(v['cve'])}</strong> — {e(v['explanation'])}</p>")

    terms = glossary_terms_used(stories, cves)
    if terms:
        parts.append("<h2>📖 Jargon decoder</h2><dl>")
        for term, desc in terms:
            parts.append(f"<dt>{e(term)}</dt><dd>{e(desc)}</dd>")
        parts.append("</dl>")

    if errors:
        parts.append(f"<p class='sub'>Sources unavailable this run: {e(', '.join(errors))}</p>")
    parts.append("<footer>Generated by <a href='https://github.com/manjou/cyberbrief'>CyberBrief</a> — "
                 "free · open source · no AI required · <a href='archive/'>archive</a></footer>")
    parts.append("</main></body></html>")
    return "".join(parts)
